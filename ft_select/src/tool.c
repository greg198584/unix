#include "ft_select.h"

char	ft_cmp_elem(const char *s1, const char *s2, char equal)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
		i++;
	if (equal)
		return (s1[i] == '=' && s2[i] == '\0');
	return ((s1[i] == s2[i] || s1[i] == ' ' || s1[i] == '\t') && s2[i] == '\0');
}
