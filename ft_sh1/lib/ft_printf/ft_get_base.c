#include "ft_printf.h"

char	*ft_get_base(int n)
{
	int		i;
	char	*s;

	i = -1;
	s = ft_strnew(n + 1);
	while (++i < n)
		*(s + i) = (i < 10) ? ('0' + i) : ('A' + (i - 10));
	return (s);
}
