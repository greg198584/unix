#include "libft.h"

char	*ft_itoa(int n)
{
	char *p;

	if (n == -2147483648)
		return ("-2147483648");
	if (!n)
		return ("0");
	p = ft_strnew(20) + 19;
	if (n >= 0 && p)
	{
		*--p = '0' + (n % 10);
		while ((n /= 10) != 0)
			*--p = '0' + (n % 10);
	}
	else if (p)
	{
		*--p = '0' - (n % 10);
		while ((n /= 10) != 0)
			*--p = '0' - (n % 10);
		*--p = '-';
	}
	return (p);
}
