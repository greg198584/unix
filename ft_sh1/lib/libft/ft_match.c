int	ft_match(char const *s1, char const *s2)
{
	if (!*s1 && !*s2)
		return (1);
	if (*s2 == '*')
	{
		if (!*s1)
			return (ft_match(s1, s2 + 1));
		return (ft_match(s1 + 1, s2) || ft_match(s1, s2 + 1));
	}
	return ((*s1 != *s2) ? 0 : ft_match(s1 + 1, s2 + 1));
}
