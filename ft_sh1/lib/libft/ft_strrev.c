#include "libft.h"

char	*ft_strrev(char *s)
{
	char	*p1;
	char	*p2;

	if (!s || !*s)
		return (s);
	p1 = s;
	p2 = s + ft_strlen(s) - 1;
	while (p2 > p1)
	{
		*p1 ^= *p2;
		*p2 ^= *p1;
		*p1++ ^= *p2--;
	}
	return (s);
}
